import { describe, it, expect } from 'vitest'
import { CZ_MESSAGES, resolveMessage } from '~/constants/messages'

/**
 * T-033: メッセージ展開の FE/BE 一致テスト (Frontend 側)
 *
 * Backend MessageResolver と同一テストケースで結果を検証する。
 * Backend テスト実行時に同一パターンで比較すること。
 */
describe('FE/BE 一致 - メッセージ展開', () => {
  // ─── パラメータ展開一致テスト ────────────────────────

  const paramCases: [string, string[], string][] = [
    ['CZ-126', ['作業日'], '作業日は必須入力です'],
    ['CZ-126', ['件名'], '件名は必須入力です'],
    ['CZ-126', ['工数'], '工数は必須入力です'],
    ['CZ-125', ['工数'], '工数はHH:MM形式で入力してください'],
    ['CZ-128', ['件名'], '件名を超える入力があります'],
    ['CZ-120', ['年度'], '年度は範囲外'],
    ['CZ-121', ['時間'], '時間のマイナス値は不可'],
    ['CZ-127', ['金額'], '金額は数値で入力してください'],
    ['CZ-130', ['上限'], '上限を超えています'],
    ['CZ-141', ['カ層'], 'カ層は業務概要としても定義されているため記述できません'],
    ['CZ-321', ['認証'], '認証の情報にエラーが発生しました'],
    ['CZ-328', ['承認'], 'ステータスを承認に変更できませんでした'],
    ['CZ-329', ['確認'], 'ステータスを確認に戻せませんでした'],
  ]

  it.each(paramCases)(
    'resolveMessage("%s", %j) → "%s"',
    (code, params, expected) => {
      expect(resolveMessage(code, params)).toBe(expected)
    },
  )

  // ─── パラメータなしメッセージ一致テスト ──────────────

  const nopCases: [string, string][] = [
    ['CZ-000', '成功完了'],
    ['CZ-101', '別ユーザーにより更新が行われたため操作できません'],
    ['CZ-102', 'サービス提供時間外（6:00〜23:30）のためデータ操作できません'],
    ['CZ-129', '工数は最小0:15以上で入力してください'],
    ['CZ-138', 'TMR番号は5文字以内の半角数字で入力してください'],
    ['CZ-144', '作業日は対象月の範囲内で入力してください'],
    ['CZ-146', '日次合計が24時間を超過しています'],
    ['CZ-147', '工数は15分単位で入力してください'],
    ['CZ-300', 'サーバー側で入力処理エラーが発生しました'],
    ['CZ-500', 'システムを終了します。よろしいですか？'],
    ['CZ-800', '対象を確認に戻しました'],
  ]

  it.each(nopCases)(
    'resolveMessage("%s") → "%s"',
    (code, expected) => {
      expect(resolveMessage(code)).toBe(expected)
    },
  )

  // ─── 未定義コードのフォールバック ────────────────────

  it('未定義コードはフォールバックメッセージを返す', () => {
    expect(resolveMessage('CZ-999')).toBe('不明なエラーが発生しました (CZ-999)')
  })

  // ─── メッセージ件数一致 ──────────────────────────────

  it('Frontend と Backend のメッセージ件数が一致する (75件)', () => {
    expect(Object.keys(CZ_MESSAGES).length).toBe(75)
  })
})
